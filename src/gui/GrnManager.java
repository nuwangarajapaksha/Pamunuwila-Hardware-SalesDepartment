    private void calculateValues() {
        try {
            int rowCount = grnTableModel.getRowCount();

            double productQty = Double.parseDouble(qtyField.getText());
            double productUnitBuyPrice = Double.parseDouble(unitBuyPriceField.getText());
            double productDiscount = Double.parseDouble(discountField.getText());
            double productVat = Double.parseDouble(vatField.getText());
            double productAmount = (productUnitBuyPrice - productDiscount + productVat) * productQty;

            amountField.setText(String.valueOf(productAmount));

            itemCountField.setText(String.valueOf(rowCount));

            double totalDiscount = 0;
            double totalVat = 0;
            double totalAmount = 0;

            for (int i = 0; i < rowCount; i++) {
                double rowQty = (double) grnTableModel.getValueAt(i, 3);

                double rowDiscount = (double) grnTableModel.getValueAt(i, 6);
                totalDiscount += rowDiscount * rowQty;

                double rowVat = (double) grnTableModel.getValueAt(i, 7);
                totalVat += rowVat * rowQty;

                double rowAmount = (double) grnTableModel.getValueAt(i, 8);
                totalAmount += rowAmount;
            }
            totalDiscountField.setText(String.valueOf(totalDiscount));
            totalVatField.setText(String.valueOf(totalVat));
            totalAmountField.setText(String.valueOf(totalAmount));
            netAmountField.setText(String.valueOf(totalAmount));

            logger.debug("Calculate values");
        } catch (NumberFormatException e) {
            logger.error("Exception: " + e.getMessage());
        }
    }

