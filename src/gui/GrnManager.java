    private void addRowButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addRowButtonActionPerformed
        // TODO add your handling code here:
        logger.debug("Add row");
        String productNo = productNoField.getText();
        String productName = productNameField.getText();
        String unitSellPrice = unitSellPriceField.getText();
        String qty = qtyField.getText();
        String unitName = unitNameField.getText();
        String unitBuyPrice = unitBuyPriceField.getText();
        String discount = discountField.getText();
        String vat = vatField.getText();
        String amount = amountField.getText();

        String PATTERN = "^[0]{0,100}[.][0]{0,100}$";
        Pattern p = Pattern.compile(PATTERN);
        Matcher m1 = p.matcher(unitSellPrice);
        Matcher m2 = p.matcher(qty);
        Matcher m3 = p.matcher(unitBuyPrice);

        logger.debug("Check validations");
        if (supplierNo == 0 || supplierNameField.equals("Select Supplier")) {
            logger.error("Supplier not selected");
            loadSupplierDialog.setVisible(true);
            Object[] supplierData = loadSupplierDialog.getSupplierData();
            supplierNo = (int) supplierData[0];
            logger.debug("Select supplier");
            supplierNameField.setText((String) supplierData[1]);
            logger.debug("Set selected supplier data to text fields");
        } else if (productNo.equals("0") || productName.equals("Select Product")) {
            logger.error("Product not selected");
            loadProductDialog.setVisible(true);

            Object[] productData = loadProductDialog.getProductData();

            String productNoo = new String(productData[0] + "");
            String productNameSet = (String) productData[1];
            String productUnitName = (String) productData[2];
            logger.debug("Select product");
            productNoField.setText(productNoo);
            productNameField.setText(productNameSet);
            unitNameField.setText(productUnitName);
            logger.debug("Set selected product data to text fields");

        } else if (m1.matches()) {
            unitSellPriceField.requestFocus(true);
            logger.error("unit sell price not entered");
        } else if (m2.matches()) {
            qtyField.requestFocus(true);
            logger.error("qty not entered");
        } else if (m3.matches()) {
            unitBuyPriceField.requestFocus(true);
            logger.error("unit buy price not entered");
        } else {
            grnTableModel.addRow(new Object[]{Integer.parseInt(productNo), productName, Double.parseDouble(unitSellPrice), Double.parseDouble(qty), unitName,
                Double.parseDouble(unitBuyPrice), Double.parseDouble(discount), Double.parseDouble(vat), Double.parseDouble(amount)});
            logger.debug("Add row successful");
            saveGrnButton.setEnabled(true);
            itemCountField.setText(String.valueOf(grnTableModel.getRowCount()));
            calculateValues();
            clearFields();
        }
        calculateValues();
    }//GEN-LAST:event_addRowButtonActionPerformed

