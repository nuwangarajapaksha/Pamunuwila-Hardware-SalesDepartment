    private void registerButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerButtonActionPerformed
        // TODO add your handling code here:
        logger.debug("Register product");
        String productNo = productNoField.getText();
        String name = nameField.getText();
        int unitNo = unitComboBox.getSelectedIndex() + 1;
        String discount = discountField.getText();
        String tax = taxField.getText();
        String image = imageField.getText();
        String description = descriptionArea.getText();
        
        logger.debug("Check validations");
        if (name.equals("")) {
            JOptionPane.showConfirmDialog(rootPane, "Please Enter The Name !", "Error", -1, JOptionPane.ERROR_MESSAGE);
            nameField.requestFocus(true);
            logger.error("product Name not entered");
        } else {
            String result = productModel.registerProduct(productNo, name, unitNo, discount, tax, image, description);
            if (result.equals("Register Successful")) {
                JOptionPane.showConfirmDialog(rootPane, result, "Message", -1, 1);
                clearFields();
                loadProductTable("");
                logger.debug("Message: " + result);
            } else {
                JOptionPane.showConfirmDialog(rootPane, result, "Error", -1, JOptionPane.ERROR_MESSAGE);
                logger.error("Error: " + result);
            }
        }
    }//GEN-LAST:event_registerButtonActionPerformed

