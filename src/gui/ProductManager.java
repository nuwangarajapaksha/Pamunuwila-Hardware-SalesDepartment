    private void productTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_productTableMouseClicked
        // TODO add your handling code here:
        clearFields();

        int row = productTable.getSelectedRow();
        int productNo = (int) productTableModel.getValueAt(row, 0);
        Object[] productData = productModel.getSelectedProductData(productNo);

        productNoField.setText(new String(productData[0] + ""));
        nameField.setText((String) productData[1]);
        unitComboBox.setSelectedItem(productData[2]);
        discountField.setText(new String(productData[3] + ""));
        taxField.setText(new String(productData[4] + ""));
        imageField.setText((String) productData[5]);
        descriptionArea.setText((String) productData[6]);

        String path = (String) productData[5];
        imageIconLoad(path);
        
        logger.debug("Load selected row data to relevant fields");

        registerButton.setEnabled(false);
        updateButton.setEnabled(true);
        removeButton.setEnabled(true);
        saveButton1.setEnabled(true);

    }//GEN-LAST:event_productTableMouseClicked

